export function getLocalizedGalleryImages(handle) {
  return LOCALIZED_GALLERY_IMAGES[handle] || [];
}

export function getProductCardImage(product) {
  const candidates = [
    ...(product?.images?.nodes || []),
    product?.selectedOrFirstAvailableVariant?.image,
    product?.featuredImage,
  ].filter(Boolean);
  const allowlist = PRODUCT_CARD_IMAGE_ALLOWLIST[product?.handle];

  if (allowlist) {
    const allowedImage = candidates.find((image) =>
      allowlist.some((filename) => image.url?.includes(filename)),
    );

    if (allowedImage) return allowedImage;
  }

  return product?.featuredImage || candidates[0];
}

export function getSelectableVariantImage(handle, variant) {
  const image = variant?.image;
  const blocklist = VARIANT_IMAGE_BLOCKLIST[handle] || [];
  const localizedVariantImage = getLocalizedVariantImage(handle, variant);
  const selectedOptionText = (variant?.selectedOptions || [])
    .map((option) => option.value)
    .join(' ')
    .toLowerCase();

  if (!image?.url) return null;

  const isBlockedImage = blocklist.some((filename) =>
    image.url.includes(filename),
  );
  const isBuyerGuideOption = /buyer|must read|guide/.test(selectedOptionText);

  if (isBuyerGuideOption) return null;
  if (isBlockedImage) return localizedVariantImage;

  return image;
}

export function filterShopifyGalleryImages(handle, images) {
  const allowlist = SHOPIFY_GALLERY_ALLOWLIST[handle];

  if (!allowlist) return images;

  return images.filter((image) =>
    allowlist.some((filename) => image.url?.includes(filename)),
  );
}

function getLocalizedVariantImage(handle, variant) {
  const imagesByColor = LOCALIZED_VARIANT_IMAGES[handle];
  if (!imagesByColor) return null;

  const color = variant?.selectedOptions
    ?.find((option) => /colou?r/i.test(option.name))
    ?.value?.trim()
    .toLowerCase();
  const image = color ? imagesByColor[color] : null;

  if (!image) return null;

  return {
    id: `localized-${handle}-${color.replace(/\s+/g, '-')}`,
    url: image.url,
    altText: `${image.label} FIE colored epee mask`,
    width: 1400,
    height: 1400,
  };
}
